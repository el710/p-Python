print('Here are examples with math methods')

int_value1 = 5
int_value2 = 7

int_value = int_value1 + int_value2
print(int_value1,'+',int_value2, ' = ', int_value)
# the same
print(5+7,type(5+7))

int_value = int_value1 - int_value2
print(int_value1,'-',int_value2, ' = ', int_value)
# the same
print(5-7,type(5-7))

int_value = int_value1 * int_value2
print(int_value1,'*',int_value2, ' = ', int_value)
# the same
print(5*7,type(5*7))

# division
real_value = int_value2 / int_value1
print(int_value2,'/',int_value1, ' = ', real_value)
# the same
print(7/5, type(7/5))

# take whole part from division
int_value = int_value2 // int_value1
print(int_value2,'//',int_value1, ' = ', int_value)
# the same
print(7//5, type(7//5))

# take whole part of the division
int_value = int_value2 // int_value1
print(int_value2,'//',int_value1, ' = ', int_value)
# the same
print(7//5, type(7//5))

# take remainder of the division
int_value = int_value2 % int_value1
print(int_value2,'%',int_value1, ' = ', int_value)
# the same
print(7%5, type(7%5))

# take power
int_value = int_value2 ** int_value1
print(int_value2,'**',int_value1, ' = ', int_value)
# the same
print(7**5, type(7**5))